num = list(range(1, 11))                    # Creating a list of numbers from 1 to 10

a = num[:5]                                 # Extracting the first five elements

b = a[::-1]                                 # Reversing the extracted elements

print("Original list:", num)                # Printing original,extracted and reversed lists
print("Extracted first five elements:", a)
print("Reversed extracted elements:", b)
